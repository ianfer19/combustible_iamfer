    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        datos.setNombre(nombre.getText());
        datos.setPlaca(placa.getText());
        datos.setCedula(cedula.getText());
        l.addElement(datos.datosConductor());
        
        datos.setCedula((nombre.getText()));
        
        int total=0;
        l.addElement("costo llenado de tanque: "+datos.costoLlenarTanque(combustible.getSelectedIndex(),Integer.parseInt(cantidadCombustible.getText())));
        l.addElement("costo balanceo: "+datos.costoBalanceo(balanceo.getSelectedIndex()));
        l.addElement("costo cambio de aceite: "+datos.cambioA(Aceite.getSelectedIndex(),cuartoaceite2.isSelected()));
        l.addElement("costo aditivos: "+datos.costoAditivos(Integer.parseInt(aditivos.getText())));
        l.addElement("costo lavado: "+datos.costoLavado(Lavado.getSelectedIndex()));
        total=datos.costoLlenarTanque(combustible.getSelectedIndex(),Integer.parseInt(cantidadCombustible.getText()))
                +datos.costoBalanceo(balanceo.getSelectedIndex())+datos.cambioA(Aceite.getSelectedIndex(),cuartoaceite2.isSelected())
                +datos.costoAditivos(Integer.parseInt(aditivos.getText()))+datos.costoLavado(Lavado.getSelectedIndex());
        l.addElement("costo total: "+total);
        l.addElement(" ");
    }//GEN-LAST:event_jButton1ActionPerformed

